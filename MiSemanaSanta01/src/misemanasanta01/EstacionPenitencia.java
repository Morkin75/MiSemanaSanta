    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        System.out.println("Has pulsado el ratón");
        System.out.println("" + evt.getX() + " " + evt.getY());
        if (contador % 2 == 0) 
        {
            x1=evt.getX();
            y1=evt.getY();
        } else {
            x2=evt.getX();
            y2=evt.getY();
        }
        contador++;
        
        this.repaint();
    }//GEN-LAST:event_formMouseClicked

