    private void jListBandasCristoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListBandasCristoValueChanged
        if(evt.getValueIsAdjusting()) { //Solo me interesa cuando la opciÃ³n estÃ¡ siendo seleccionada, no me interesa cuando la opciÃ³n se desselecciona
            if(jRadioButtonAM.isSelected()) {
                System.out.println("AM" + jListBandasCristo.getSelectedIndex());
                jLabelCiudadBanda.setText(VentanaPrincipal.arrayAgrupaciones[jListBandasCristo.getSelectedIndex()].getCiudad());
                jLabelComponenteBanda.setText("" + VentanaPrincipal.arrayAgrupaciones[jListBandasCristo.getSelectedIndex()].getComponentes());             
                jLabelPrecioBanda.setText("" + VentanaPrincipal.arrayAgrupaciones[jListBandasCristo.getSelectedIndex()].getPrecio());
                if(VentanaPrincipal.arrayAgrupaciones[jListBandasCristo.getSelectedIndex()].isTipo())
                        jLabelTipoBanda.setText("Banda de CCTT");
                else 
                    jLabelTipoBanda.setText("AgrupaciÃ³n Musical");
            }
            if(jRadioButtonCCTT.isSelected()) {
                System.out.println("CT" + jListBandasCristo.getSelectedIndex());
                jLabelCiudadBanda.setText(VentanaPrincipal.arrayCCTT[jListBandasCristo.getSelectedIndex()].getCiudad());
                jLabelComponenteBanda.setText("" + VentanaPrincipal.arrayCCTT[jListBandasCristo.getSelectedIndex()].getComponentes());                
                jLabelPrecioBanda.setText("" + VentanaPrincipal.arrayCCTT[jListBandasCristo.getSelectedIndex()].getPrecio());
                if(VentanaPrincipal.arrayCCTT[jListBandasCristo.getSelectedIndex()].isTipo())
                        jLabelTipoBanda.setText("Banda de CCTT");
                else 
                    jLabelTipoBanda.setText("AgrupaciÃ³n Musical");
            }
        }
    }//GEN-LAST:event_jListBandasCristoValueChanged


